//java program of a star pattern

package corejava;

 class StarPattern {
 public static void main(String[]args) {
	 //creating the variables
	 int i ,j;
	//creating the for loop
    for(i=0 ; i<4; i++ ) {
    	
    	for(j=0 ; j<11; j++
    			) {
    		//printing the element
        	System.out.print("*");
        	
        }
    	//printing the element
    	System.out.println("");
    }
	 
 }
}
